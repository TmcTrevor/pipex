#include "libft.h"

int		ft_word_count(const char *s, char e)
{
	int i;
	int c;

	i = -1;
	c = -1;
	if (*s)
	{
		c = 1;
		while (s[++i] == e)
		{
			if (s[i + 1] == '\0')
				return (0);
		}
		while (s[++i])
		{
			if (s[i] == e)
			{
				if (s[i + 1] != '\0' && s[i + 1] != e)
					c++;
			}
		}
	}
	return (c);
}
